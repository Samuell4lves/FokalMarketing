create table if not exists public.usuarios (
  id bigint primary key,
  nome text not null,
  nicho text not null default '',
  nome_empresa text not null default '',
  email text not null unique,
  senha text not null,
  telefone text not null default '',
  tipo text not null check (tipo in ('Administrador', 'Cliente')),
  plano_tipo text not null default 'Producao de Conteudo' check (plano_tipo in ('Producao de Conteudo', 'Gestao de Trafego', 'Ambos'))
);

create table if not exists public.eventos (
  id bigint generated by default as identity primary key,
  titulo text not null,
  descricao text not null default '',
  data date not null,
  hora time not null,
  tipo text not null default 'tarefa',
  status text not null default 'agendada',
  repeticao_tipo text not null default 'none',
  repeticao_intervalo integer not null default 1,
  repeticao_unidade text not null default 'none',
  repeticao_dias_semana text not null default '',
  repeticao_dia_mes integer,
  repeticao_fim date,
  evento_pai_id bigint references public.eventos(id) on delete cascade,
  data_original date,
  tipo_excecao text
);

create table if not exists public.eventos_clientes (
  evento_id bigint not null references public.eventos(id) on delete cascade,
  cliente_id bigint not null references public.usuarios(id) on delete cascade,
  primary key (evento_id, cliente_id)
);

create table if not exists public.conteudos (
  id bigint generated by default as identity primary key,
  titulo text not null,
  tipo text not null,
  descricao text not null default '',
  link_drive text not null default '',
  data_publicacao date not null,
  data_criacao date not null default current_date,
  status text not null default 'pendente',
  cliente_id bigint not null references public.usuarios(id) on delete cascade
);

create table if not exists public.campanhas (
  id bigint generated by default as identity primary key,
  nome text not null,
  cliente_id bigint not null references public.usuarios(id) on delete cascade,
  orcamento numeric(14,2) not null default 0,
  plataforma text not null,
  data_inicio date not null,
  data_fim date not null,
  cliques integer not null default 0,
  impressoes integer not null default 0,
  conversoes integer not null default 0,
  custo_resultado numeric(14,2) not null default 0
);

create table if not exists public.financeiro_lancamentos (
  id bigint generated by default as identity primary key,
  nome text not null,
  descricao text not null default '',
  valor numeric(14,2) not null default 0,
  tipo text not null
);

create index if not exists idx_usuarios_tipo on public.usuarios(tipo);
create index if not exists idx_eventos_data_hora on public.eventos(data, hora);
create index if not exists idx_eventos_pai_original on public.eventos(evento_pai_id, data_original);
create index if not exists idx_eventos_clientes_cliente on public.eventos_clientes(cliente_id);
create index if not exists idx_conteudos_cliente_data on public.conteudos(cliente_id, data_publicacao);
create index if not exists idx_campanhas_cliente_periodo on public.campanhas(cliente_id, data_inicio, data_fim);

