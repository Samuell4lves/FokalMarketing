do $$
begin
  if exists (
    select 1 from information_schema.tables
    where table_schema = 'public'
      and table_name = 'tarefas'
  ) then
    insert into public.eventos (
      id,
      titulo,
      descricao,
      data,
      hora,
      tipo,
      status
    )
    select
      t.id,
      t.titulo,
      coalesce(t.descricao, ''),
      t.data,
      t.hora,
      coalesce(t.tipo, 'tarefa'),
      coalesce(t.status, 'agendada')
    from public.tarefas t
    on conflict (id) do nothing;
  end if;

  if exists (
    select 1 from information_schema.tables
    where table_schema = 'public'
      and table_name = 'tarefas_clientes'
  ) then
    insert into public.eventos_clientes (evento_id, cliente_id)
    select tarefa_id, cliente_id
    from public.tarefas_clientes
    on conflict do nothing;
  end if;
end $$;
